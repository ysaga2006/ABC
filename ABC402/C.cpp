#include <bits/stdc++.h>
using ll = long long;
using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int n, m; cin >> n >> m;
    vector<vector<int>> v(m, vector<int>());
    for (;m--;) {
        int k; cin >> k;
        for (;k--;) {
            int a; cin >> a;
            v.at(m).emplace_back();
        }
    }
    return 0;
}